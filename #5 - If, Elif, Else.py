#Python 5: If, Elif and Else Statements

#If and Elif (else if) are used to work out if a certain variable is satisfied. Else can be used to specify what will happen if the variable does not satisfy any of the if statements. For example:

number = int(input("Pick a number between 1 and 10: "))

if number == 2:
    print("Cool, that's the only even prime number!")
elif number == 1:
    print("Number 1 was a pretty cool choice")
else:
    print("Nice choice I guess. There are better numbers")

#If should be used for the first time there is an if statement in a set of if statements. After the first time, elif (ELSE if) should be used.
#Else statements will only be used once the if statements have been completed and if the variable does not satisfy any of the if statements

